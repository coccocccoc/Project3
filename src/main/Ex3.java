package main;

public class Ex3 {

	public static void main(String[] args) {
		
		int result1 = 10 + 2; // 12
		int result2 = 10 - 2; // 8
		int result3 = 10 * 2; // 20
		int result4 = 10 / 2; // 5
		int result5 = 9 % 2; // 1 => 9는 홀수다 (짝수, 홀수, 배수 문제에서 자주 사용)
		
		int result6 = 9 % 3; // 0 => 9는 3의 배수이다
		int result7 = 8 % 3; // 1 => 8은 3의 배수가 아니다
		
	}

}
